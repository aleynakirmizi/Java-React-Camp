package homework1;

public class User {
    private int nationalNumber;
    private String first_name;
    private String last_name;
    private String registiration_date;

    public int getNationalNumber() {
        return nationalNumber;
    }

    public void setNationalNumber(int nationalNumber) {
        this.nationalNumber = nationalNumber;
    }

    public String getFirst_name() {
        return first_name;
    }

    public void setFirst_name(String first_name) {
        this.first_name = first_name;
    }

    public String getLast_name() {
        return last_name;
    }

    public void setLast_name(String last_name) {
        this.last_name = last_name;
    }

    public String getRegistiration_date() {
        return registiration_date;
    }

    public void setRegistiration_date(String registiration_date) {
        this.registiration_date = registiration_date;
    }
}
